// Generate baked SVG county geometry for US states.
//
// Downloads the us-atlas county TopoJSON (cached locally after first run),
// filters to one state by FIPS, projects to a per-state aspect-ratio viewBox,
// and emits a TypeScript file with one { name, d } entry per county. Geometry
// is static, so this runs once (re-run only to add a state or change sizing).
//
// Usage:
//   node scripts/gen-state-county-geometry.mjs <stateFips> <slug> [width]
//   node scripts/gen-state-county-geometry.mjs 01 alabama
//   node scripts/gen-state-county-geometry.mjs all        # every state + DC
//
// Output: lib/data/state-geometry/<slug>.ts

import { writeFileSync, existsSync, readFileSync, mkdirSync } from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { geoMercator, geoConicEqualArea, geoPath } from "d3-geo";
import { feature } from "topojson-client";

const ATLAS_URL = "https://cdn.jsdelivr.net/npm/us-atlas@3/counties-10m.json";
const CACHE = join(tmpdir(), "us-atlas-counties-10m.json");

// FIPS -> slug, matching web/lib/state-config + the legacy page slugs.
const STATES = {
  "01": "alabama", "02": "alaska", "04": "arizona", "05": "arkansas",
  "06": "california", "08": "colorado", "09": "connecticut", "10": "delaware",
  "11": "district-of-columbia", "12": "florida", "13": "georgia", "15": "hawaii",
  "16": "idaho", "17": "illinois", "18": "indiana", "19": "iowa", "20": "kansas",
  "21": "kentucky", "22": "louisiana", "23": "maine", "24": "maryland",
  "25": "massachusetts", "26": "michigan", "27": "minnesota", "28": "mississippi",
  "29": "missouri", "30": "montana", "31": "nebraska", "32": "nevada",
  "33": "new-hampshire", "34": "new-jersey", "35": "new-mexico", "36": "new-york",
  "37": "north-carolina", "38": "north-dakota", "39": "ohio", "40": "oklahoma",
  "41": "oregon", "42": "pennsylvania", "44": "rhode-island", "45": "south-carolina",
  "46": "south-dakota", "47": "tennessee", "48": "texas", "49": "utah",
  "50": "vermont", "51": "virginia", "53": "washington", "54": "west-virginia",
  "55": "wisconsin", "56": "wyoming",
};

const PAD = 12;

// Base (unfitted) projection per state. Default is Mercator, which is fine for
// the lower-48 + Hawaii. Alaska is special-cased: its Aleutian chain straddles
// the antimeridian (180°), so Mercator throws those islands to the far edge of
// the frame and squashes the rest of the state into a thin ~15%-wide strip.
// Use the Albers conic that d3's geoAlbersUsa uses for its Alaska inset, which
// keeps the chain contiguous and gives a true-to-shape, full-height map.
function baseProjection(fips) {
  if (fips === "02") {
    return geoConicEqualArea().rotate([154, 0]).parallels([55, 65]);
  }
  return geoMercator();
}

async function loadAtlas() {
  if (existsSync(CACHE)) {
    return JSON.parse(readFileSync(CACHE, "utf8"));
  }
  const res = await fetch(ATLAS_URL);
  if (!res.ok) throw new Error(`Failed to download atlas: ${res.status}`);
  const text = await res.text();
  writeFileSync(CACHE, text);
  return JSON.parse(text);
}

function generate(allCounties, fips, slug, W) {
  const fc = {
    type: "FeatureCollection",
    features: allCounties.features
      .filter((f) => String(f.id).padStart(5, "0").startsWith(fips))
      .sort((a, b) => String(a.properties.name).localeCompare(String(b.properties.name))),
  };
  if (fc.features.length === 0) throw new Error(`No counties for FIPS ${fips}`);

  // Derive the state's natural aspect ratio so the viewBox is tight (no
  // letterboxing of wide states into a portrait box).
  const probe = baseProjection(fips).fitWidth(W - 2 * PAD, fc);
  const bounds = geoPath(probe).bounds(fc); // [[x0,y0],[x1,y1]]
  const aspect = (bounds[1][1] - bounds[0][1]) / (bounds[1][0] - bounds[0][0]);
  const H = Math.round((W - 2 * PAD) * aspect) + 2 * PAD;

  const projection = baseProjection(fips).fitExtent(
    [[PAD, PAD], [W - PAD, H - PAD]],
    fc,
  );
  const path = geoPath(projection);
  const counties = fc.features.map((f) => ({ name: String(f.properties.name), d: path(f) }));

  const missing = counties.filter((c) => !c.d);
  if (missing.length) throw new Error(`No path for: ${missing.map((c) => c.name).join(", ")}`);

  const banner = `// AUTO-GENERATED by scripts/gen-state-county-geometry.mjs — do not edit by hand.
// Source: us-atlas counties-10m (US Census cartographic boundaries).
// State FIPS ${fips} · ${counties.length} counties · viewBox ${W}x${H}.
`;
  const body = `${banner}
export interface CountyGeometry {
  /** County name with no "County" suffix, matching get_state_accident_summary.county. */
  name: string;
  /** SVG path 'd' attribute, projected into the viewBox below. */
  d: string;
}

export const VIEWBOX = { width: ${W}, height: ${H} } as const;

export const COUNTY_GEOMETRY: CountyGeometry[] = ${JSON.stringify(counties, null, 2)};
`;
  mkdirSync("lib/data/state-geometry", { recursive: true });
  writeFileSync(`lib/data/state-geometry/${slug}.ts`, body);
  return counties.length;
}

const [, , arg1, arg2, arg3] = process.argv;
if (!arg1) {
  console.error("Usage: node scripts/gen-state-county-geometry.mjs <stateFips> <slug> [width] | all");
  process.exit(1);
}

const atlas = await loadAtlas();
const allCounties = feature(atlas, atlas.objects.counties);

if (arg1 === "all") {
  const W = Number(arg2) || 700;
  let total = 0;
  for (const [fips, slug] of Object.entries(STATES)) {
    const n = generate(allCounties, fips, slug, W);
    total += 1;
    console.log(`  ${slug}: ${n} counties`);
  }
  // Registry of lazy importers so the shared v2 client can load one state's
  // geometry by slug without bundling all 51 into every page.
  const slugs = Object.values(STATES);
  const entries = slugs
    .map((s) => `  "${s}": () => import("./${s}"),`)
    .join("\n");
  const registry = `// AUTO-GENERATED by scripts/gen-state-county-geometry.mjs — do not edit by hand.
import type { CountyGeometry } from "./alabama";

export type GeometryModule = {
  COUNTY_GEOMETRY: CountyGeometry[];
  VIEWBOX: { readonly width: number; readonly height: number };
};

/** slug -> lazy import of that state's baked county geometry (code-split per state). */
export const GEOMETRY_LOADERS: Record<string, () => Promise<GeometryModule>> = {
${entries}
};
`;
  writeFileSync("lib/data/state-geometry/registry.ts", registry);
  console.log(`Wrote ${total} state geometry files + registry.ts.`);
} else {
  const fips = String(arg1).padStart(2, "0");
  const slug = arg2 || STATES[fips];
  if (!slug) throw new Error(`No slug for FIPS ${fips}; pass one explicitly.`);
  const W = Number(arg3) || 700;
  const n = generate(allCounties, fips, slug, W);
  console.log(`Wrote lib/data/state-geometry/${slug}.ts: ${n} counties.`);
}
