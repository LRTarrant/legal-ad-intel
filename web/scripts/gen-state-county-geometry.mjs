// Generate baked SVG county geometry for a single US state.
//
// Downloads the us-atlas county TopoJSON, filters to one state by FIPS,
// projects to a fixed viewBox, and emits a TypeScript file with one
// { name, d } entry per county. Geometry is static, so this runs once
// (re-run only to add a new state or change the projection size).
//
// Usage:
//   node scripts/gen-state-county-geometry.mjs <stateFips> <slug> [width] [height]
//   node scripts/gen-state-county-geometry.mjs 01 alabama
//
// Output: lib/data/state-geometry/<slug>.ts

import { writeFileSync } from "node:fs";
import { geoMercator, geoPath } from "d3-geo";
import { feature } from "topojson-client";

const ATLAS_URL = "https://cdn.jsdelivr.net/npm/us-atlas@3/counties-10m.json";

const [, , fipsArg, slugArg, wArg, hArg] = process.argv;
if (!fipsArg || !slugArg) {
  console.error("Usage: node scripts/gen-state-county-geometry.mjs <stateFips> <slug> [width] [height]");
  process.exit(1);
}
const stateFips = String(fipsArg).padStart(2, "0");
const slug = slugArg;
const W = Number(wArg) || 640;
const H = Number(hArg) || 820;

const res = await fetch(ATLAS_URL);
if (!res.ok) {
  console.error(`Failed to download atlas: ${res.status}`);
  process.exit(1);
}
const topo = await res.json();

const allCounties = feature(topo, topo.objects.counties);
const stateCounties = {
  type: "FeatureCollection",
  features: allCounties.features
    .filter((f) => String(f.id).padStart(5, "0").startsWith(stateFips))
    .sort((a, b) => String(a.properties.name).localeCompare(String(b.properties.name))),
};

if (stateCounties.features.length === 0) {
  console.error(`No counties found for FIPS ${stateFips}`);
  process.exit(1);
}

// Pad so the shape doesn't touch the viewBox edge.
const PAD = 12;
const projection = geoMercator().fitExtent(
  [
    [PAD, PAD],
    [W - PAD, H - PAD],
  ],
  stateCounties,
);
const path = geoPath(projection);

const counties = stateCounties.features.map((f) => ({
  name: String(f.properties.name),
  d: path(f),
}));

const missing = counties.filter((c) => !c.d);
if (missing.length) {
  console.error("Counties with no path:", missing.map((c) => c.name));
  process.exit(1);
}

const banner = `// AUTO-GENERATED by scripts/gen-state-county-geometry.mjs — do not edit by hand.
// Source: us-atlas counties-10m (US Census cartographic boundaries).
// State FIPS ${stateFips} · ${counties.length} counties · viewBox ${W}x${H}.
`;

const body = `${banner}
export interface CountyGeometry {
  /** County name with no "County" suffix, matching get_state_accident_summary.county. */
  name: string;
  /** SVG path 'd' attribute, projected into the viewBox below. */
  d: string;
}

export const VIEWBOX = { width: ${W}, height: ${H} } as const;

export const COUNTY_GEOMETRY: CountyGeometry[] = ${JSON.stringify(counties, null, 2)};
`;

const outPath = `lib/data/state-geometry/${slug}.ts`;
writeFileSync(outPath, body);
console.log(`Wrote ${outPath}: ${counties.length} counties.`);
console.log("Sample names:", counties.slice(0, 5).map((c) => c.name).join(", "));
