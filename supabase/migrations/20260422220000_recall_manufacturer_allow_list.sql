-- ============================================================================
-- Day 5: Commercial-manufacturer allow-list
-- ----------------------------------------------------------------------------
-- Reason: the OpenFDA-sourced recall_manufacturers table was seeded using the
-- first word of each firm name, producing noise entries like 'Noah', 'Hamilton',
-- 'Depot', 'Family Dollar Stores' that pollute CourtListener case searches.
--
-- This migration:
--   1. Adds is_commercial_manufacturer (default false) so the CL search loop
--      can filter to curated mfrs only.
--   2. Adds cl_search_name: a clean query string used for CL search, derived
--      from the canonical_name with address/country junk stripped.
--   3. Seeds 778 known-good medical-device manufacturers generated from the
--      existing recall_manufacturers contents via Day 5 classification logic
--      (see pipeline/seeds/manufacturer_allow_list.csv).
-- ============================================================================

