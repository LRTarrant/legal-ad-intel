  add column if not exists is_commercial_manufacturer boolean not null default false;

alter table public.recall_manufacturers
  add column if not exists cl_search_name text;

create index if not exists recall_manufacturers_commercial_idx
  on public.recall_manufacturers (is_commercial_manufacturer)
  where is_commercial_manufacturer = true;

